import { Modal } from "bootstrap";
export default {
    name: 'Modal',
    props: ["title", "mode", "body", "close_handle"],
    data() {
        return {
            isEnable_illustration: false,   //客户说要屏蔽掉插画，这里软件屏蔽
            isHidden_SVG_OK: true,
            isHidden_SVG_ERROR: true,
            watch_mode: ""
        }
    },
    methods: {
        open() {
            //静态传参方法打开模态框

            // 获取模态框元素
            const modalEl = document.getElementById('exampleModal');
            // 创建一个Bootstrap Modal对象
            const myModal = new Modal(modalEl)
            //设置预设状态插画
            if (this.mode == "OK") this.isHidden_SVG_OK = false;
            else if (this.mode == "ERROR") this.isHidden_SVG_ERROR = false;
            // 调用对象的show()方法打开模态框
            if (this.mode != "")
                myModal.show()
        },
        process_open() {
            //动态传参方法打开模态框

            // 获取模态框元素
            const modalEl = document.getElementById('exampleModal');
            // 创建一个Bootstrap Modal对象
            const myModal = new Modal(modalEl)
            //设置预设状态插画
            if (this.watch_mode == "OK") this.isHidden_SVG_OK = false;
            else if (this.watch_mode == "ERROR") this.isHidden_SVG_ERROR = false;
            // 调用对象的show()方法打开模态框
            myModal.show()
        }
    },
    watch: {
        mode: function (newData, oldData) {
            this.watch_mode = newData;
            this.process_open();
        }
    }

